# String Concatenation
str1 = "Hello"
str2 = "World"
str3 = str1 + " " + str2
print(str3)

# String Replication
str4 = "Python "
str5 = str4 * 3
print(str5)

# String Slicing
str6 = "Welcome to Python"
print(str6[0:7])
print(str6[7:])

# String Methods
str7 = "Python is fun"
print(str7.upper())
print(str7.lower())
print(str7.replace("fun", "powerful"))
print(str7.split(" "))

# String Formatting
name = "John"
age = 20
str8 = "My name is {} and I am {} years old".format(name, age)
print(str8)
